export const convertToMonth = (num : number) => {
    if(num === 1) return 'Jan'
    if(num === 2) return 'Feb'
    if(num === 3) return 'Mar'
    if(num === 4) return 'Apr'
    if(num === 5) return 'May'
    if(num === 6) return 'June'
    if(num === 7) return 'July'
    if(num === 8) return 'Aug'
    if(num === 9) return 'Sept'
    if(num === 10) return 'Oct'
    if(num === 11) return 'Nov'
    if(num === 12) return 'Desc'

    return ''
}